//Odd Number
for (let i = 0; i <= 10; i++) {
  if (i % 2 !== 0) console.log(i);
}
// Odd Number Reverse

for (let i=10; i>=1; i--){
    if(i % 2 !== 0) console.log(i);
}

//Even Number
for (let i = 0; i <= 10; i++) {
  if (i % 2 == 0) console.log(i);
}

// Odd Number Reverse
for (let i=10; i>=1; i--){
    if(i % 2 == 0) console.log(i);
}